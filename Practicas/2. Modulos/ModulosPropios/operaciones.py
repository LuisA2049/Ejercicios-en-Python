def calculadora(numero1, numero2,op):
    sumar= numero1+numero2
    restar= numero1-numero2
    multi= numero1*numero2
    divi= numero1+numero2
    if op=="suma":
        return sumar
    if op=="restar":
        return restar
    if op=="multi":
        return multi
    if op=="divi":
        return divi